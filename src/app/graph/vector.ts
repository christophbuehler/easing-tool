export const dot = (v1: number[], v2: number[]) => [v1[0] * v2[0], v1[1] * v2[1]];
export const div = (v1: number[], v2: number[]) => [v1[0] / v2[0], v1[1] / v2[1]];
export const plus = (v1: number[], v2: number[]) => [v1[0] + v2[0], v1[1] + v2[1]];
export const minus = (v1: number[], v2: number[]) => [
  v1[0] - v2[0],
  v1[1] - v2[1],
];
